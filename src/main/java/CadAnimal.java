    private void CadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CadastrarActionPerformed
    // TODO add your handling code here:
    List<JTextField> campos = List.of(txtNome, txtRaca, txtDataNascimento, txtNomeTutor, txtCpfTutor);

    if (Utilitarios.validaCampos(campos)) {
        return;
    }
        if(!update){
            String cpf = campos.get(4).getText();
            Tutor tutor = DadosApp.clinica.getTutores(cpf);

            if (tutor == null) {
                JOptionPane.showMessageDialog(null, "Tutor do animal n√£o encontrado!", "Tutor n√£o encontrado!", JOptionPane.WARNING_MESSAGE);
                return;
            }

            Animal newAnimal = new Animal(
                campos.get(0).getText(), 
                campos.get(1).getText(), 
                campos.get(2).getText(), 
                tutor,
                new ArrayList<>()      
            );

            tutor.getAnimais().add(newAnimal);
            JOptionPane.showMessageDialog(null, "Animal cadastrado com sucesso!", "Confirma√ß√£o", JOptionPane.INFORMATION_MESSAGE);

        }
        else {   
                modificarDados(animalEditado);
        }
        
        
        
        this.dispose();

    }//GEN-LAST:event_CadastrarActionPerformed

    private void btmExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btmExcluirActionPerformed
        // TODO add your handling code here:
        int resposta = JOptionPane.showConfirmDialog(
            this,
            "Tem certeza que deseja excluir este Animal?",
            "Confirmar Exclus√£o",
            JOptionPane.YES_NO_OPTION,
            JOptionPane.WARNING_MESSAGE
        );

        if (resposta == JOptionPane.YES_OPTION) {
            String nomeAnimal = txtNome.getText().trim(); // nome do animal
            String cpfTutor = txtCpfTutor.getText().trim(); // CPF do tutor

            boolean animalRemovido = false;

            for (Tutor t : DadosApp.clinica.getTutores()) {
                if (t.getCpf().equals(cpfTutor)) {
                    // Procura o animal dentro da lista do tutor
                    Animal animalParaRemover = null;
                    for (Animal a : t.getAnimais()) {
                        if (a.getNome().equalsIgnoreCase(nomeAnimal)) {
                            animalParaRemover = a;
                            break;
                        }
                    }

                    if (animalParaRemover != null) {
                        t.getAnimais().remove(animalParaRemover);
                        animalRemovido = true;
                    }

                    break; // CPF √© √∫nico, pode sair do loop
                }
            }

            if (animalRemovido) {
                JOptionPane.showMessageDialog(this, "Animal exclu√≠do com sucesso!");
                this.dispose(); // Fecha a tela
            } else {
                JOptionPane.showMessageDialog(this, "Animal ou Tutor n√£o encontrado.");
            }
        }
    }//GEN-LAST:event_btmExcluirActionPerformed

    public void modificarDados(Animal an){
        an.setNome(txtNome.getText());
        an.setRaca(txtRaca.getText());
        an.setDataNascimento(txtDataNascimento.getText());

        JOptionPane.showMessageDialog(null, "Dados do animal atualizados com sucesso!", "Atualiza√ß√£o", JOptionPane.INFORMATION_MESSAGE);
    }

 
    
